    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (this.jTable1.getSelectedRow() != -1) {
            this.editor.setText(PistasCompiladas.getInstancePistasActivacion().getPistas().get(this.jTable1.getSelectedRow()).getCodigoFuente());
            JOptionPane.showMessageDialog(null, "Codigo Fuente listo para ser editado, Suerte!!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar una fila para editarla");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (this.jTable1.getSelectedRow() != -1) {
            int respuesta = JOptionPane.showConfirmDialog(null, "Seguro de eliminar la Lista?", "Confirmaci√≥n", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (respuesta == JOptionPane.YES_OPTION) {
                PistasCompiladas.getInstancePistasActivacion().getPistas().remove(this.jTable1.getSelectedRow());
                PistasCompiladas.getInstancePistasActivacion().guardarEnBinario();
                JOptionPane.showMessageDialog(null, "Eliminado exitosamente :)");
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar una fila para editarla");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

